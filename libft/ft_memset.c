#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	char	*b1;
	size_t	i;

	b1 = (char *)b;
	i = 0;
	while (i < len)
	{
		b1[i] = c;
		++i;
	}
	return (b);
}
