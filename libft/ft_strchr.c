#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*copy;

	copy = (char *)s;
	while (*copy++ != c)
	{
		if (*copy == '\0' && c != '\0')
			return (0);
	}
	return (copy - 1);
}
